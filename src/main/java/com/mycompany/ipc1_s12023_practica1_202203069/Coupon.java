public class Coupon {
    private String name;
    private Float discount;

    public Coupon(){
        name="";
        discount=0f;
    }
    
    public Coupon(String name, Float discount) {
        this.name = name;
        this.discount = discount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getDiscount() {
        return discount;
    }

    public void setDiscount(Float discount) {
        this.discount = discount;
    }
    
    
}
